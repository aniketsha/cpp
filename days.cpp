#include<iostream>
using namespace std;
main()
{
	int a;
	cout<<"enter one-digit number";
	cin>>a;
	if(a==0)
	{
		cout<<"its sunday";
	}
	else if(a==1)
	{
		cout<<"its monday";
	}
	else if(a==2)
	{
		cout<<"its tuesday";
	}
	else if(a==3)
	{
		cout<<"its wednesday";
	}
	else if(a==4)
	{
		cout<<"its thursday";
	}
	else if(a==5)
	{
		cout<<"its friday";
	}
	else
	{
		cout<<"its saturday";
	}
}
